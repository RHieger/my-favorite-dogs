const name = 'sAm';
const first = name.charAt(0);

const upper = first.toUpperCase();

console.log(`\nname = ${name}\n`);
console.log(`Capturing first character of name...\n`);
console.log(`First character of ${name} = ${first}\n`);

console.log('Transforming first character to uppercase...\n');
console.log(`Uppercase \'s\' = ${upper}\n`);
